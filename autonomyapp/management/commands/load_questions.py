class Command(BaseCommand):
    help = 'Загружает вопросы в базу данных'
 
    def handle(self, *args, **kwargs):
        for q in QUESTIONS:
            obj, created = Question.objects.get_or_create(
                card_number=q['card_number'],
                defaults=q
            )
            if created:
                self.stdout.write(f'✓ Добавлена карточка #{q["card_number"]} {q["topic"]}')
            else:
                self.stdout.write(f'— Карточка #{q["card_number"]} уже существует')
 
        self.stdout.write(self.style.SUCCESS(f'\nГотово! Всего вопросов в БД: {Question.objects.count()}'))